class Student:
    def __init__(self, student_id, student_login, student_name, student_surname, student_password,
                 student_date_of_registration, student_device, student_privileges, tg_login):
        self.student_id = student_id
        self.student_login = student_login
        self.student_name = student_name
        self.student_surname = student_surname
        self.student_password = student_password
        self.student_date_of_registration = student_date_of_registration
        self.student_device = student_device
        self.student_privileges = student_privileges
        self.tg_login = tg_login
